document.addEventListener("DOMContentLoaded", function () {
    var btnSuaTrangCaNhan = document.getElementById("btnSuaTrangCaNhan");
    if (btnSuaTrangCaNhan) {
        btnSuaTrangCaNhan.addEventListener("click", function (event) {
            event.preventDefault(); // Ngăn chặn hành vi mặc định của button
            var element = document.querySelector(".change");
            if (element) {
                element.style.display = "flex";
            }
        });
    }
});
